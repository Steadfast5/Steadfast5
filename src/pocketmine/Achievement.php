		"buildWorkBench" => [
			"name" => "Benchmarking",
			"requires" => [
				"mineWood"
			]
		],
		"buildPickaxe" => [
			"name" => "Time to Mine!",
			"requires" => [
				"buildWorkBench"
			]
		],
		"buildFurnace" => [
			"name" => "Hot Topic",
			"requires" => [
				"buildPickaxe"
			]
		],
		"acquireIron" => [
			"name" => "Acquire hardware",
			"requires" => [
				"buildFurnace"
			]
		],
		"buildHoe" => [
